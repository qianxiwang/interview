package annotation;

@PKBean(table = "t_animal")
public class Animal {

    private Integer id;
    private String name;


    @PKField(value = "gender")
    private String sex;

    public Animal() {
    }

    public Animal(Integer id, String name, String sex) {
        this.id = id;
        this.name = name;
        this.sex = sex;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
